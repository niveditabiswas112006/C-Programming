// 8. W.A.P a program to store and display different data types.
#include <iostream>
using namespace std;
int main(){
    char ch = 'a';
    double d = 5.6;
    bool b = true;
    int i = 10;
    cout<<"character: "<<ch <<endl;
    cout<<"double: "<<d <<endl;
    cout<<"boolean: "<<(b? "true":"false")<<endl;
    cout<<"integer: "<<i <<endl;
    return 0;
}
